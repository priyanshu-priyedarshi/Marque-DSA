package Pattern;

public class NumericalHalfPyramid {
    public static void main(String[] args){
        for(int line = 1; line <= 4; line++){
            for(int number = 1; number <= line; number++){     //bruhh it's damn simple 
                System.out.print(number);
            }
            System.out.println();
        }
    }
}
